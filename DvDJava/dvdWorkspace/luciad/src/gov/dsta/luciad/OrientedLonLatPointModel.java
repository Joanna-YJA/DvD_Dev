package gov.dsta.luciad;

import com.luciad.util.ILcdOriented;

public class OrientedLonLatPointModel extends LonLatPointModel implements ILcdOriented {

	private double fOrientation;

	public OrientedLonLatPointModel(double aLon, double aLat){
		super(aLon, aLat);
	}
	
	@Override
	public double getOrientation() {
		return fOrientation;
	}

	public void setOrientation(double aOrientation) {
		fOrientation = aOrientation;
	}


	
	
}
